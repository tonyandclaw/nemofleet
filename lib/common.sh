# ── container resolution (survives sandbox rebuilds: id changes each rebuild) ──
resolve_ct() {  # $1 = name fragment (team-lead | worker-a | worker-b)
  docker ps --format '{{.Names}}' 2>/dev/null | grep -m1 "openshell-$1-" || true
}

# primary_ip — the host's primary LAN IPv4, interface-name-agnostic. Uses the source IP the kernel
# would pick for outbound traffic (default route), so it works whether the NIC is eth0 / enP7s7 /
# ens5 / wlan0 (hardcoding "eth0" broke TLS cert signing on a DGX Spark whose NIC is enP7s7).
# Falls back to the first global-scope non-docker/bridge address. Empty only if truly offline.
primary_ip() {
  local ip
  ip="$(ip -4 route get 1.1.1.1 2>/dev/null | sed -n 's/.* src \([0-9.][0-9.]*\).*/\1/p' | head -1)"
  [ -n "$ip" ] || ip="$(ip -4 -o addr show scope global up 2>/dev/null | awk '$2 !~ /^(docker|br-|veth)/ {print $4}' | cut -d/ -f1 | head -1)"
  printf '%s' "$ip"
}
CT_LEAD="$(resolve_ct "$TEAMLEAD_CT_NAME")"
CT_WA="$(resolve_ct "$WORKERA_CT_NAME")"
CT_WB="$(resolve_ct "$WORKERB_CT_NAME")"
CT_WC="$(resolve_ct "$WORKERC_CT_NAME")"

require_ct() {  # warn if a needed container is missing. $1=varname $2=human name
  local var="$1" name="$2"
  if [ -z "${!var}" ]; then
    echo "[lib] WARN: $name container not found (rebuilt? stopped?). docker ps | grep $name" >&2
    return 1
  fi
  return 0
}

# ── EBG19P device access (single source of truth: services/bridge/ebg19p.py) ──
# One login per call; emits a JSON object. Used by the ebg19p-*-sync.sh scripts so the
# login / asus_token handling is never re-implemented per script.
ebg_nvget() {  # $1=cred_file, $2..=nvram keys → JSON {key: value}
  python3 "$BRIDGE_DIR/ebg19p.py" "$1" nvget "${@:2}"
}
ebg_hooks() {  # $1=cred_file, $2..=appGet hooks → JSON {hook: raw}
  python3 "$BRIDGE_DIR/ebg19p.py" "$1" hooks "${@:2}"
}

# ── SkillOS 治理閘(docs/design/skill-curation.md)────────────────────────────
# skill_gate <SKILL.md path> [name] — 技能落地前送 worker-c POST /skill-review 審(品質閘+抗膨脹)。
# 回 0=approve、1=reject(綁定,呼叫端勿落地)。worker-c 未部署/不可達 → 0+提示
# (gate-if-available:自我進化不因治理節點缺席而停擺;部署 zone C 後閘自動生效)。
skill_gate() {
  local f="$1" name="${2:-}" tok payload resp
  [ -n "$CT_WC" ] || { echo "[skill-gate] worker-c 未部署 → 未審放行(部署 zone C 後自動生效)" >&2; return 0; }
  tok="$(cat "$BRIDGE_TOKEN_FILE" 2>/dev/null || true)"
  [ -n "$tok" ] || { echo "[skill-gate] 無 bridge token → 未審放行" >&2; return 0; }
  payload=$(python3 -c 'import json,sys; print(json.dumps({"op":"insert","text":open(sys.argv[1],encoding="utf-8").read()}))' "$f") || return 0
  resp=$(printf '%s' "$payload" | docker exec -i "$CT_WC" sh -c "curl -s -m10 -X POST -H 'X-Bridge-Token: $tok' -H 'Content-Type: application/json' -d @- http://127.0.0.1:9099/skill-review" 2>/dev/null || true)
  case "$resp" in
    *'"verdict": "approve"'*|*'"verdict":"approve"'*)
      echo "[skill-gate] ✓ worker-c approve${name:+($name)}"; return 0 ;;
    *'"verdict": "reject"'*|*'"verdict":"reject"'*)
      echo "[skill-gate] ✗ worker-c REJECT${name:+($name)} — 不落地。required_fixes:" >&2
      printf '%s' "$resp" | python3 -c 'import sys,json
try:
    for x in json.load(sys.stdin).get("required_fixes", []): print("  -", x, file=sys.stderr)
except Exception: pass' >&2 || true
      return 1 ;;
    *) echo "[skill-gate] worker-c 未回應 → 未審放行" >&2; return 0 ;;
  esac
}

