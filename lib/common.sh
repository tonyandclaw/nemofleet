BRIDGE_TOKEN_FILE="$BRIDGE_DIR/.bridge-token"   # team-lead → worker delegation token
APPROVAL_KEY_FILE="$BRIDGE_DIR/.approval-key"   # HMAC signing key for worker-c high-risk actions (rollback/firmware-apply); shared with team-lead's approval_issue.py only — see wi_approval.py
